// komparasi adalah membandingkan dua nilai

let a = 30;
let b = 20;

console.log(a > b) // menentukan nilai besar dari
console.log(a >= b) // membandingkan nilai pertama besar atau sama dengan dari nilai kedua
console.log(a < b) // menentukan nilai kecil dari
console.log(a <= b) // membandingkan nilai pertama kecil atau sama dengan dari nilai kedua 
console.log(a == b) // menentukan nilai apakah sama
console.log(a != b) // menentukan nilai apakah tidak sama
console.log(a === b) // menentukan nilai identik
console.log(a !== b) // menentukan nilai tidak identik

// operator logika

const firstValue = 20;
const secondValue = 30;

console.log(firstValue > 15 && secondValue < 15); // && adalah nilai and
console.log(firstValue > 18 || secondValue < 18); // || adalah nilai or
console.log(!(firstValue > 19)); // ! adalah nilai not
console.log(!(firstValue > 20 && secondValue < 20));